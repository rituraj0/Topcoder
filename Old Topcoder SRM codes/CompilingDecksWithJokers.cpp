class CompilingDecksWithJokers
{
public:


int maxCompleteDecks(vector <int> in, int joke)
{
    vector<ll> cards;

    for(int i=0;i<in.sz;i++)
         cards.pb( in[i] );

    ll jokers=joke;


    ll ans=0;

    ll lo=0,hi=(1LL<<50);

    while(lo<=hi)
    {

        ll mid=(lo+hi)/2;

        ll req=0;

        for(int i=0;i<cards.sz;i++)
              req+=max(0LL,mid-cards[i]);

        if( (req <=mid)&& (req<=jokers) )
        {
            ans=mid;
            lo=mid+1;
        }
       else
       {
           hi=mid-1;
       }
    }

    return ans;

}

};


