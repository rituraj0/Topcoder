class WeirdSort
{
public:

vector <int>  sortArray(vector <int> data)
{
    vector<int> ans;

    sort( data.begin(), data.end());

    while( data.size() )
    {

         for(int i=0;i<data.size();i++)
         {
              if( ans.size() && ans.back()+1==data[i] )
                continue;

              if( data[0]==data[i] && data[i]+1==data.back() )
                continue;

                ans.pb( data[i] );
                data.erase( data.begin()+i);
                break;

         }
    }

    return ans;

}

};


