class PickTwoCards
{
public:


double equalProbability(string in)
{
    int cont[500]; fill(cont,0);

    int n=sz(in);

    for(int i=0;i<n;i++)
         cont[ in[i] ]++;

    double all=n*(n-1)/2;

    double curr=0;


    for(int i=0;i<500;i++)
         curr+=cont[i]*(cont[i]-1)/2;

    return curr/all;

}

};


