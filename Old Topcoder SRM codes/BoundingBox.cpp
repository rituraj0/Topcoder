class BoundingBox 
{
public:


int smallestArea(vector <int> X, vector <int> Y) 
{
  int x=*max_element(X.bg,X.en)-*min_element(X.bg,X.en);
  int y= *max_element(Y.bg,Y.en)-*min_element(Y.bg,Y.en);
  
  return (x*y);
	
}

};


