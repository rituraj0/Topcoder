class HanoiState
{
public:


string partwayState(string target, int moves)
{
     int n=target.size();

    string curr(n,'A');

    for(int i=n-1;i>=0;i--)
    {
        if( target[i]==curr[i])
             continue;
         int req=(1<<i);

         char tp=(3 - (target[i]-'A') - (curr[i]-'A') )+'A';

         if( req <=moves)
         {
             moves-=req;
             curr[i]=target[i];

             for(int j=0;j<i;j++)
                 curr[j]=tp;
         }
         else
         {
                 for(int j=0;j<i;j++)
                  target[j]=tp;
         }

    }

    return curr;

}

};



