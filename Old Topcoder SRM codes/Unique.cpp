class Unique
{
public:


string removeDuplicates(string S)
{
	map<char,bool> done;
	string ans="";

	for(int i=0;i<S.sz;i++)
         if( done.count(S[i])==0 )
    {
        done[ S[i] ]=true;
        ans+=S[i];
    }

    return ans;
}

};


