class PairGame
{
public:


     vector< pair<int,int> > all;

    map < pair<int,int> , bool > mapa;

    void solve( int x , int a, int b)
    {

          while( (a >=1) && (b >=1) )
          {
                if(x)
                     all.pb( mp(a,b));
                else
                     mapa[ mp(a,b) ]=true;

                if( a-b > 0)
                {
                    a=a-b;
                }
                else
                {
                    b=b-a;
                }
          }
    }


int maxSum(int a, int b, int c, int d)
{
     solve(0,a,b);
     solve(1,c,d);

     for(int i=0;i<all.sz;i++)
         if( mapa[ all[i] ])
           return all[i].X+all[i].Y;

     return -1;


}

};


