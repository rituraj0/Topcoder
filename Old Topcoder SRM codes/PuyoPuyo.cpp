const ll mod = 1000000007LL;

class PuyoPuyo
{
public:

ll dp[1005][1015];

int theCount(int L, int N)
{
    fill(dp,0);

    dp[0][0]=1;

    for(int i=0;i<N;i++)
    {
          //add on an empty clinder
          dp[i+1][L-1]=( dp[i+1][L-1] + dp[i][0]*4LL )%mod;

          // add one of diffrent color color
          for(int j=1;j+L-1<=N;j++)//j +L-1<=N-1 bhi chalega
              dp[i+1][j+L-1]=( dp[i+1][j+L-1] + dp[i][j]*3LL )%mod;

           //add of same color
           for(int j=1;j<=N;j++)
              dp[i+1][j-1]=( dp[i+1][j-1] + dp[i][j] )%mod;
    }

    return dp[N][0];
}

};


