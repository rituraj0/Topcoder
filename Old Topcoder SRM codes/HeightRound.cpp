class HeightRound
{
public:


vector <int> getBestRound(vector <int> in)
{
	sort( in.begin() , in.end());
	int n=in.size();

	for(int d=0;d<1000;d++)
    {
        bool mila=true;
        bool used[55]; fill(used,0);

        vector<int> ans(n);
        ans[0]=in[0];

        int prev=in[0];
        int pid=0;
        used[0]=true;
        int curr=1;

        int here=n;

        while( prev!=in[n-1])
        {
             if( in[curr]>d+prev)
             {
                 if( curr > pid+1)
                 {
                     curr--;
                     here--;
                     ans[here]=in[curr];
                     prev=in[curr];
                     pid=curr;
                     used[curr]=true;
                 }
                else
                {
                    mila=false;
                     break;
                }
             }

             curr++;

             if(curr==n)
                 break;
        }

        int i=1;

        if( mila)
        {
            for(int q=0;q<n;q++)
                 if( !used[q])
                   ans[i++]=in[q];

            int mx=ans[n-1]-ans[0];

            for(int j=1;j<n;j++)
                  mx=max( mx, ans[j]-ans[j-1]);

            if( mx <=d)
                return ans;
        }

    }

    vector<int> ans;
    return ans;
}

};


