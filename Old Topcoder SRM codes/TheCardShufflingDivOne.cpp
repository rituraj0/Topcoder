class TheCardShufflingDivOne
{
public:


int shuffle(int n, int left, int right)
{
   int cd=n%2;

   if(cd==0)
     cd=2;

   int rem=cd;

   for( ; cd < n;)
   {    cd+=2;
        int numl=cd/2 + cd%2;
        int numr=cd/2;

        if( rem <=numr-1)
        {
            int id=numr-rem+1;
            id+=(right%numr);
            if(id>numr)
                id-=numr;

            rem=(numr-id+1)*2;
        }
        else
        {
            int id=numl-(rem-numr+1)+1;
            id+=(left%numl);
            if( id > numl)
            {
                id-=numl;
            }
            rem=(numl-id+1)*2-1;
        }
   }

   return rem;
}

};


