class RemissiveSwaps
{
public:

    int memo[1<<20];

    string tost( int in)
    {
        char ans[10];
        sprintf(ans,"%d",in);
        return ans;
    }

    int solve( int in)
    {
        if( memo[in]!=-1)
           return memo[in];

        int ans=in;

         string curr=tost(in);
         int n=curr.sz;

         for(int i=0;i<n;i++)
         {
              if( curr[i]=='0')
                 continue;

                for(int j=i+1;j<n;j++)
                {
                     if( curr[j]=='0')
                         continue;

                     curr[i]--;
                     curr[j]--;
                     swap( curr[i] , curr[j]);
                     int nw=atoi(curr.c_str() );
                     swap( curr[i] , curr[j]);
                     curr[i]++;
                     curr[j]++;

                     ans=max( ans , solve(nw ) );
                }
         }

         return memo[in]=ans;

}


int findMaximum(int N)
{
    fill(memo,-1);

    return solve(N);
}

};


