class AlienAndGame
{
public:

int  getNumber(vector <string> in)
{
    int ans=0;

    int row=in.size() , col=in[0].size();

   for(int i=0;i<row;i++)
      for(int j=0;j<col;j++)
   {
        int all=55;

       for(int k=i;k<row;k++)
       {
            char st=in[k][j];

               int cn=0;

               for(int m=j;m<col;m++)
                  if(in[k][m]!=st)
                     break;
                else
                    cn++;

                all=min( all , cn);

                if(all==k-i+1)
                ans=max( ans , all*(k-i+1) );
       }
   }

   return ans;
}

};


