class LotteryPyaterochka
{
public:


double chanceToWin(int N)
{
    double n=N;
    n=n*5.0;
    double nicha=n*(n-1.0)*(n-2.0)*(n-3.0)*(n-4.0);


    double ans=0;

    if( N > 1)
    {
        ans+=600.00*(n-5.0)*(n-6.0);//koe 3 ek sath
        ans+=600.00*(n-5);//4 ek sath
    }

    ans+=120.00;//5 ek sath

    ans=ans/nicha;
    ans=ans*(double)N;

    return ans;
}

};


