vector<string> comp( vector<string> a, vector<string> b )
{
      if( a.sz < b.sz)
          return a;

      if( a.sz > b.sz)
          return b;

      return min(a,b);

}

class GuitarConcert
{
public:


vector <string> buyGuitars(vector <string> guitarNames, vector <string> guitarSongs)
{

     int n=guitarNames.sz;

     int cont=0;
     vector<string> ans;

     int s=guitarSongs[0].sz;


     for(int mask=0;mask<(1<<n);mask++)
     {
            set<int> st;
            vector<string> vs;

            for(int i=0;i<n;i++)
                  if( mask & (1<<i) )
                    {
                        vs.pb(guitarNames[i]);

                        for(int j=0;j<s;j++)
                          if( guitarSongs[i][j]=='Y')
                            st.insert(j);
                    }

            sort( vs.bg,vs.en);

           if( st.sz > cont)
           {
                cont=st.sz;
                ans=vs;
           }
          else if( st.sz==cont)
          {
              ans=comp( ans , vs);
          }

     }

     return ans;



}

};


