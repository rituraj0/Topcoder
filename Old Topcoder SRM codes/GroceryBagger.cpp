class GroceryBagger
{
public:

map<string,int> mapa;

int minimumBags(int st, vector <string> in)
{
      int n=sz(in);

      for(int i=0;i<n;i++)
         mapa[ in[i] ]++;

       int ans=0;

       for( map<string,int>::iterator it=mapa.begin();it!=mapa.end();it++)
       {

           int x=(*it).Y;

           ans+=(x+st-1)/st;
       }

       return ans;
}

};


