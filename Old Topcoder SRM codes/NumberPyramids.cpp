ll mod=1000000009LL;

class NumberPyramids
{
public:

ll memo[2][1<<20];
int N;
vector<int> val;

ll solve( int id , int sm)
{
     if(sm<0) return 0;

     if(id==N+1)
     {
         return (sm==0);
     }

     if( memo[id&1][sm]!=-1) return memo[id&1][sm];

      int ans=memo[id&1][sm]=0;

      if( sm-val[id]>=0)
        { ans=(ans+solve(id,sm-val[id]) )%mod;
          ans=(ans+solve(id+1,sm-val[id]) )%mod;
        }

     return memo[id&1][sm]=ans;

}

int count(int base, int top)
{
    if(base>20) return 0;

    N=base-1;

    int ncr[25][25];
    fill(ncr,0);
    for(int i=0;i<=N;i++)
        ncr[i][0]=1;
    for(int i=1;i<=N;i++)
        for(int j=1;j<=N;j++)
         ncr[i][j]=ncr[i-1][j-1]+ncr[i-1][j];


    for(int i=0;i<=N;i++)
        val.pb( ncr[N][i]);

    for(int i=N;i>=0;i--)
    {
        fill(memo[i&1],-1);
        for(int j=0;j<=top;j++)
            solve(i,j);
    }

    return solve(0,top);



}

};


