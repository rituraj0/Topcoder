class LocateTreasure
{
public:


string location(int k, int multi)
{
   if(k>12)
   {
       k=k%12;

       if(k<=2) k+=12;
     }

     int x=0,y=0;
     int d=1;

     for(int i=0;i<k;i++)
     {
         x+=dx[i%4]*d;
         y+=dy[i%4]*d;

         d=d*multi;

         while(d>9)
          d-=9;
       }

     return tost(x)+" "+tost(y);

}

};



