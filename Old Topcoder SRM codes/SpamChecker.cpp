class SpamChecker
{
public:


string spamCheck(string in, int g, int b)
{


    int sm=0;

    int n=in.sz;

    for(int i=0;i<n;i++)
    {
          if( in[i]=='x')
             sm-=b;
          else
             sm+=g;

          if(sm <0)
             return "SPAM";
    }

   return  "NOT SPAM";

}

};

