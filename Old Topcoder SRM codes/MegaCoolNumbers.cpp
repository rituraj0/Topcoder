#define mod 1000000007

class MegaCoolNumbers
{
public:


ll dp[1005][11][11][11];//i , pow,diff, last

int count(int N, int A)
{
   fill(dp,0);

    if( A > 9)  return 0;

    for(int d=1;d<=9;d++)
         dp[1][1][9][d]=1;

    for(int i=1;i<=N;i++)
         for(int p=1;p<=A;p++)
            for(int last=1;last<=9;last++)
            {

                  for(int d=last;d<=9;d++)
                  {
                      dp[i+1][p][d-last][d]=( dp[i+1][p][d-last][d]+ dp[i][p][9][last])%mod;
                  }

                  for(int diff=0;diff<=8;diff++)
                     for(int d=last;d<=9;d++)
                       if( d-last==diff)
                        {
                            dp[i+1][p][diff][d]=(dp[i+1][p][diff][d]+ dp[i][p][diff][last])%mod;
                        }
                       else if( p < 9)
                       {
                           dp[i+1][p+1][9][d]=( dp[i+1][p+1][9][d] + dp[i][p][diff][last] )%mod;
                       }

            }


            ll ans=0;

            for(int last=1;last<=9;last++)
                 for(int diff=0;diff<=9;diff++)
                   ans=(ans + dp[N][A][diff][last] )%mod;

            return ans;




}

};


