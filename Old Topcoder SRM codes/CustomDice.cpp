#define mod 1000000007

int  dp[2][6000000];

class CustomDice
{
public:


int countDice(int M)
{
      int m=6*M-21;

      if(m<0)  return 0;

      fill(dp,0);

      for(int i=0;i<=m;i++)
         dp[0][i]=1;

      for(int i=1;i<=6;i++)
         for(int j=0;j<=m;j++)
      {
            dp[i%2][j]=dp[1-(i%2)][j];

             if(j>=i)
                 dp[i%2][j]+=dp[i%2][j-i];

                 dp[i%2][j]=dp[i%2][j]%mod;
      }

      ll ans=dp[0][m];

       ans=(ans*30)%mod;

       return ans;
}

};


