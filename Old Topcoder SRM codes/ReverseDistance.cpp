class ReverseDistance
{
public:


string tost( ll in)
{
    char ans[20];
    sprintf(ans,"%lld",in);
    return ans;
}


 ll rever(ll x)
 {
         ll r =0;

         while(x>0)
         {
             r=r*10+x%10;
             x/=10;
         }
         return r;
 }

string find(int diff)
{

   for(int i=1;i<200000;i++)
   {
        string P=tost(i);
        int len=P.sz;

        ll rev=rever(i);
        ll val=diff+rev;


        for(int tl=2*len-1;tl<=2*len;tl++)
        {

             string Q=tost(val);

             int req=tl-len;

             while( Q.size() > req)
                 Q=Q.substr(1);

             while( Q.size() < req )
                  Q="0"+Q;

             string curr=P+Q;

             ll tp=atoll(curr.c_str());

             if( tp - rever(tp)==diff)
                 return curr;

        }


   }

      return "NONE";



}

};


