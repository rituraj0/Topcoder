class RandomSwaps
{
public:

double  getProbability(int arrayLength, int swapCount, int a, int b)
{

    double same=(double)( (arrayLength-1)*(arrayLength-2) )/(double)( arrayLength*(arrayLength-1));

    double here=1.0,there=0.0;

    for(int i=1;i<=swapCount;i++)
    {
        here=here*same + there*(1-same)/(double)(arrayLength-1);

        there=(1-here);
    }

    if(a==b)
        return here;

    return there/(double)(arrayLength-1);

}

};

