class Nisoku
{
public:


double theMax(vector <double> in)
{
    sort(in.bg,in.en);

    int n=sz(in);

    double ans=1.0;

    for(int s=0;s<=n;s+=2)
    {
        double tp=1.0;

          for(int i=s;i<n;i++)
            tp*=in[i];

          for(int i=0;i<s-i-1;i++)
             tp*=(in[i]+in[s-1-i]);

          ans=max(ans,tp);
    }

    return ans;

}

};


