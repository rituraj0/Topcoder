class HappyLetterDiv1
{
public:

vector<int> zerofree( vector<int>  in)
{
    vector<int> ans;

    for(int i=0;i<in.sz;i++)
         if( in[i]!=0)
          ans.pb( in[i]);

    return ans;
}
string getHappyLetters(string in)
{
    vector<int> cont(26,0);

    int n=in.sz;

    for(int i=0;i<n;i++)
         cont[ in[i]-'a']++;

    string ans="";

    for(int i=0;i<26;i++)
    {
        vector<int> curr;

        if(cont[i]==0) continue;

        for(int j=0;j<26;j++)
             if(i!=j)
              curr.pb(cont[j]);

            curr=zerofree(curr);

            while( curr.size() > 1)
            {
                sort( curr.bg,curr.en);

                int cn=curr.sz;
                curr[cn-1]-=curr[cn-2];
                curr[cn-2]=0;

                curr=zerofree(curr);
            }

          if( curr.sz==0)
            ans+=('a'+i);
          else if( curr[0] < cont[i])
            ans+=('a'+i);
    }

    return ans;

}

};


