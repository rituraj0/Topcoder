class SilverbachConjecture 
{
public:

bool comp[1<<15];

vector <int> solve(int n) 
{
  fill(comp,0);
   
   for(int i=2;i<=n;i++)
      {
         for(int j=2;j<i;j++)
            if( i%j==0)
              comp[i]=true;
        }
  
    for(int i=2;i<n;i++)
      for(int j=2;j<n;j++)
         if( i+j==n)
           if( comp[i] && comp[j] )
               {
                 vector<int> ans;
                 ans.pb(i);
                 ans.pb(j);
                 return ans;
                 }
         
          
	
}

};


