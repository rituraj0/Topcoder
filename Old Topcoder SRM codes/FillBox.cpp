class FillBox
{
public:


int minCubes(int length, int width, int height, vector <int> cubes)
{
     ll L=length,W=width,H=height;

     ll done=0;
     ll ans=0;

     for(int i=cubes.sz-1;i>=0;i--)
     {
         ll len=(1LL<<i);

         ll req=(L/len)*(W/len)*(H/len);
            req-=done;

         ll used=min( req ,(ll)cubes[i] );

           done=(done+used)*8;

           ans+=used;
     }

     if( done < 8*L*W*H)
         return -1;

         return ans;

}

};


