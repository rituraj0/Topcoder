class KeysInBoxes
{
public:

ll dp[55][55];

string getAllKeys(int N, int M)
{
     fill(dp,0); dp[1][1]=1;

     for(int i=2;i<=N;i++)
          for(int j=1;j<=20;j++)
             dp[i][j]=dp[i-1][j-1]+(i-1)*dp[i-1][j];

     ll ap=1;
     for(ll i=1;i<=N;i++)
          ap=ap*i;

     ll gd=0;
     for(int i=1;i<=M;i++)
           gd+=dp[N][i];

     ll gc=__gcd(ap,gd);

     gd=gd/gc;
     ap=ap/gc;

     char ans[55];
     sprintf(ans,"%lld/%lld",gd,ap);

     return ans;

}

};


