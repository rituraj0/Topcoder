class DifferentStrings 
{
public:



int minimize(string A, string B) 
{
   int n=sz(A);
   int m=sz(B);
   
   int ans=100000;
   
   for(int st=0;st<m;st++)
   {
       if( st +  n -1 >= m-1 )
         continue;
      
         int tp=0;
            
         for(int i=0;i<n;i++)
           if( A[i]!=B[st+i] )
             tp++;
             
             ans=min(ans,tp);
      }
         
         return ans;
	
}

};


